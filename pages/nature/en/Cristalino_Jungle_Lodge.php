	<p>
		Located in the south of the Amazon, next to a quiet river, Cristalino Jungle Lodge is a secret refuge where the passion for unparalleled service is surpassed only by the hectic desire of the lodge to set the standard in conservation and sustainable tourism. Reach this charming jungle destination through the canoe and discover the natural wealth of the region at a relaxing pace.
		Cristalino Jungle Lodge is an award-winning, environmentally-friendly lodge set in a private reserve next to the Cristalino State Park. It has been voted one of the best hotels in Brazil by National Geographic Traveler
		
	</p>
	<p>
		We are guessing that it is not easy to create an elegant but sustainable lodge in the middle of the Amazon, especially when said lodge is only accessible via a river boat ride. But despite the inherent difficulties, Cristalino Lodge is pulling eco-luxury like we have never seen before.
		
	</p>
	<p>
		Do not misunderstand us. You will not find amenities like an infinity pool, a fully stocked minibar, air conditioning or satellite TV. (We would question the lodge's commitment to sustainability if you did.) What you will find are comfortable bungalows integrated harmoniously with the jungle; Private indoor and outdoor showers; A delightfully comfortable bed; A desk; A private balcony with an idyllic hammock; And surprisingly fast Wi-Fi from the comfort of your bed. How do they manage to have super-fast internet in the middle of the jungle when so often we struggle to find it for the most mundane tasks in big cities that we will never understand.
		
	</p>
	<p>
		Among the jungle explorations, relax on the Cristalino River on the lodge's floating dock, an ideal spot for sunbathing and swimming. Meals at Cristalino Jungle Lodge are served in a buffet style under a cozy thatched-roof patio, and dinner is served by candlelight every evening. Climb the Galvanized Canopy Tower to observe the different layers of the forest and see the jungle come alive at dawn and dusk - an impressive burst of activity 15 stories high.
		
	</p>
	<p>
		In the center of Cristalino Lodge is the magnificent restaurant, bar and campfire.
		
	</p>
	<p>
		The restaurant is best described as spacious and serene, built of dark wood, floor to ceiling windows and surrounded by dense jungle and huge natural rocks. Meals are self-service buffet and offer Brazilian cuisine with fresh and organic products. 
		
