	<h2>Asa Wright Nature Centre (AWNC)</h2>
	<p>
		This Not-for-Profit Trust was created in 1967 by a group of naturalists and birdwatchers from Trinidad and was one of the first nature centers to be established throughout the Caribbean region.
	</p>
	<p>
		As well as being a center for conservation and research in tropical ecology, Asa Wright is a world-class birding destination. On the grounds you can observe a large number of bird species, including jacobin with white neck, tufted coquette, collared trogon and bearded bellbird.
	</p>
	<p>
		A trip to Trinidad should include a stay at AWNC. Here, you can sit on the terrace sipping coffee or tea while watching a kaleidoscope of tropical birds feed on the trees. The fruits of the tremor tree are an obvious favorite of tanagers, honeycreepers, banana quits, and others. A quick walk along the Discovery Trail takes you to white-bearded leks and golden manakins and territories of the bearded vociferous bellbird.
	</p>

	<p>
		If you stay three or more nights at AWNC, you can visit the resident Oilbird Colony in Dunston's Cave. The oleaginous bird is a rare nocturnal bird that eats fruit, which uses echolocation to navigate and forage in the woods at night. Apart from these peculiar features, this species also has very specific nesting requirements, since it is raised and spread mainly in caves. The AWNC colony, currently stand at 131 birds, is one of the safest oilbird colonies on the island and a major conservation initiative for the center.
	</p>
	<p>
		The center is composed of several rooms, each with a bathroom, located in the main estate house (which has disabled access), as well as in the nearby cottages that are scattered throughout the attractive garden-like grounds. In total there are 24 rooms; the accommodation is simple but perfectly comfortable. The main house's terrace has beautiful views across the forest that extends through the valley, and this setting is one of the best birding spots in the Caribbean.
	</p>
